def filter_switch_raguard_policy_data(json):
    option_list = ['device_role', 'managed_flag', 'match_prefix',
                   'match_src_addr', 'max_hop_limit', 'max_router_preference',
                   'min_hop_limit', 'name', 'other_flag']

    json = remove_invalid_fields(json)
    dictionary = {}

    for attribute in option_list:
        if attribute in json and json[attribute] is not None:
            dictionary[attribute] = json[attribute]

    return dictionary


def underscore_to_hyphen(data):
    if isinstance(data, list):
        for i, elem in enumerate(data):
            data[i] = underscore_to_hyphen(elem)
    elif isinstance(data, dict):
        new_data = {}
        for k, v in data.items():
            new_data[k.replace('_', '-')] = underscore_to_hyphen(v)
        data = new_data

    return data


def switch_raguard_policy(data, fos, check_mode=False):
    state = data['state']
    switch_raguard_policy_data = data['switch_raguard_policy']
    filtered_data = underscore_to_hyphen(filter_switch_raguard_policy_data(switch_raguard_policy_data))

    # check_mode starts from here
    if check_mode:
        diff = {
            "before": '',
            "after": filtered_data,
        }
        mkey = fos.get_mkey('switch', 'raguard-policy', filtered_data)
        current_data = fos.get('switch', 'raguard-policy', mkey=mkey)
        is_existed = current_data and current_data.get('http_status') == 200 \
            and isinstance(current_data.get('results'), list) \
            and len(current_data['results']) > 0

        # 2. if it exists and the state is 'present' then compare current settings with desired
        if state == 'present' or state is True:
            if mkey is None:
                return False, True, filtered_data, diff

            # if mkey exists then compare each other
            # record exits and they're matched or not
            if is_existed:
                is_same = is_same_comparison(
                    serialize(current_data['results'][0]), serialize(filtered_data))
                return False, not is_same, filtered_data, {"before": current_data['results'][0], "after": filtered_data}

            # record does not exist
            return False, True, filtered_data, diff

        if state == 'absent':
            if mkey is None:
                return False, False, filtered_data, {"before": current_data['results'][0], "after": ''}

            if is_existed:
                return False, True, filtered_data, {"before": current_data['results'][0], "after": ''}
            return False, False, filtered_data, {}

        return True, False, {'reason: ': 'Must provide state parameter'}, {}

    if state == "present" or state is True:
        return fos.set('switch',
                       'raguard-policy',
                       data=filtered_data,
                       )

    elif state == "absent":
        return fos.delete('switch',
                          'raguard-policy',
                          mkey=filtered_data['name'])
    else:
        fos._module.fail_json(msg='state must be present or absent!')


def is_successful_status(resp):
    return 'status' in resp and resp['status'] == 'success' or \
        'http_status' in resp and resp['http_status'] == 200 or \
        'http_method' in resp and resp['http_method'] == "DELETE" and resp['http_status'] == 404


def fortiswitch_switch(data, fos, check_mode):
    fos.do_member_operation('switch', 'raguard-policy')
    current_cmdb_index = fos.monitor_get('/system/status')['cmdb-index']
    if data['switch_raguard_policy']:
        resp = switch_raguard_policy(data, fos, check_mode)
    else:
        fos._module.fail_json(msg='missing task body: %s' % ('switch_raguard_policy'))
    if check_mode:
        return resp
    return not is_successful_status(resp), \
        is_successful_status(resp) and \
        current_cmdb_index != resp['cmdb-index'], \
        resp, {}


