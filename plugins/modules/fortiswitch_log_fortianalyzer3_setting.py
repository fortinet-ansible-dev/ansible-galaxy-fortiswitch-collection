def filter_log_fortianalyzer3_setting_data(json):
    option_list = ['__change_ip', 'address_mode', 'buffer_max_send',
                   'conn_timeout', 'enc_algorithm', 'encrypt',
                   'fdp_device', 'fdp_interface', 'hmac_algorithm',
                   'ips_archive', 'localid', 'max_buffer_size',
                   'mgmt_name', 'override', 'psksecret',
                   'server', 'source_ip', 'status',
                   'upload_day', 'upload_interval', 'upload_option',
                   'upload_time']

    json = remove_invalid_fields(json)
    dictionary = {}

    for attribute in option_list:
        if attribute in json and json[attribute] is not None:
            dictionary[attribute] = json[attribute]

    return dictionary


def underscore_to_hyphen(data):
    if isinstance(data, list):
        for i, elem in enumerate(data):
            data[i] = underscore_to_hyphen(elem)
    elif isinstance(data, dict):
        new_data = {}
        for k, v in data.items():
            new_data[k.replace('_', '-')] = underscore_to_hyphen(v)
        data = new_data

    return data


def log_fortianalyzer3_setting(data, fos):
    log_fortianalyzer3_setting_data = data['log_fortianalyzer3_setting']
    filtered_data = underscore_to_hyphen(filter_log_fortianalyzer3_setting_data(log_fortianalyzer3_setting_data))

    return fos.set('log.fortianalyzer3',
                   'setting',
                   data=filtered_data,
                   )


def is_successful_status(resp):
    return 'status' in resp and resp['status'] == 'success' or \
        'http_status' in resp and resp['http_status'] == 200 or \
        'http_method' in resp and resp['http_method'] == "DELETE" and resp['http_status'] == 404


def fortiswitch_log_fortianalyzer3(data, fos):
    fos.do_member_operation('log.fortianalyzer3', 'setting')
    current_cmdb_index = fos.monitor_get('/system/status')['cmdb-index']
    if data['log_fortianalyzer3_setting']:
        resp = log_fortianalyzer3_setting(data, fos)
    else:
        fos._module.fail_json(msg='missing task body: %s' % ('log_fortianalyzer3_setting'))

    return not is_successful_status(resp), \
        is_successful_status(resp) and \
        current_cmdb_index != resp['cmdb-index'], \
        resp, {}


